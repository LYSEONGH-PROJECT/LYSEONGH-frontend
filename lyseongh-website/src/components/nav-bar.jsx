function MobileLink({
  href,
  onOpenChange,
  className,
  children,
  ...props
}) {
  const router = usePathname()
  return (
    <Link
      href={href}
      onClick={() => {
        onOpenChange?.(false)
      }}
      className={cn(className)}
      {...props}
    >
      {children}
    </Link>
  )
}